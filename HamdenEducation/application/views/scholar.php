                <p>The Hamden Education Foundation administers one scholarship for students at Hamden Middle School and several scholarships for students graduating from Hamden High School.

Scholarship for Middle School Students
Judy Baierl DeMaio Memorial Scholarship Fund:

This scholarship fund was established by family, friends, and colleagues in memory of Judy Baierl DeMaio, a beloved teacher at the Hamden Middle School for 32 years, who often donated money to children in need. Sometimes she bought shoes for children who needed them, helped a family who suffered in a fire, or bought Christmas presents for someone who might not have any. In keeping with that spirit, following Judy’s death in 2007, her sisters set up a scholarship fund with HEF for the following purposes:

Up to $750 per year can be given to one or more middle school students or families in need for any of the following reasons: sickness, death in family, fire or other emergency need, food, clothing, housing, education, drug or alcohol treatment, therapy, or a need similar to the above. The money is to go directly to benefit a student and/or his/her family.

Working with faculty and staff, the Middle School principal advises HEF of potential candidates for this scholarship assistance.

Scholarships for seniors graduating from Hamden High School
We currently administer six scholarships for seniors graduating from Hamden High School. Applications for the scholarships are available from the Hamden High School guidance department and are due in the spring of the student’s senior year:

Alida D. Begina, Ed.D. Memorial Scholarship: Funded originally by generous donations from family, friends, and colleagues of former Superintendent Alida Begina, and now funded by the Hamden Education Foundation, this scholarship is awarded annually to a graduating senior from Hamden High School who has demonstrated superior academic performance and participation in community activities and who plans to pursue a career in education.

Alex McGraw Memorial Scholarship: Funded by generous donations from family and friends in honor of Alex McGraw.  These scholarships will be awarded annually to two graduating seniors from Hamden High School who have demonstrated an interest in music as Alex had, financial need, participation in community and school activities and plan to study music in college.

Anthony F. Mentone Memorial Scholarship: Funded by generous donations from family, friends, and colleagues in memory of beloved high school baseball coach Anthony F. Mentone, this scholarship is awarded annually to a graduating senior from Hamden High School who played baseball for the high school and participated in community activities.

Bettye J. Middleton Memorial Scholarship: Funded by generous donations from family, friends, and colleagues in memory of beloved Church Street School teacher Bettye J. Middleton, who taught in Hamden for 36 years, this scholarship is awarded annually to a graduating senior from Hamden High School who had attended Church Street School, plans to continue his or her education at an accredited four-year university with a major in Early Childhood or Elementary Education, and has demonstrated high academic performance and participation in activities and/or community service. Preference will be given to students with a financial need.   Academic improvement during the senior year will also be a consideration.

Franklin Foundation Scholarship: Funded by generous donations from the Franklin Philanthropic Foundation, these scholarships are awarded annually to graduating seniors from Hamden High School who have demonstrated superior academic performance and financial need.</p>
